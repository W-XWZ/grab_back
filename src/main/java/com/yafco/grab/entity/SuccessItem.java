package com.yafco.grab.entity;

public class SuccessItem {
	private Integer settleReqId;
	private String classId;
	private String whId;
	private Integer qty;
	private Integer realQty;
	private String message;
	public Integer getSettleReqId() {
		return settleReqId;
	}
	public void setSettleReqId(Integer settleReqId) {
		this.settleReqId = settleReqId;
	}
	public String getClassId() {
		return classId;
	}
	public void setClassId(String classId) {
		this.classId = classId;
	}
	public String getWhId() {
		return whId;
	}
	public void setWhId(String whId) {
		this.whId = whId;
	}
	public Integer getQty() {
		return qty;
	}
	public void setQty(Integer qty) {
		this.qty = qty;
	}
	public Integer getRealQty() {
		return realQty;
	}
	public void setRealQty(Integer realQty) {
		this.realQty = realQty;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	
}
