package com.yafco.grab.entity;

public class PickEty {
	private Integer settleReqId;
	private Integer qty;
	private String sFirmId;
	private String sCustomerId;
	private String whId;
	public Integer getSettleReqId() {
		return settleReqId;
	}
	public void setSettleReqId(Integer settleReqId) {
		this.settleReqId = settleReqId;
	}
	public Integer getQty() {
		return qty;
	}
	public void setQty(Integer qty) {
		this.qty = qty;
	}
	public String getsFirmId() {
		return sFirmId;
	}
	public void setsFirmId(String sFirmId) {
		this.sFirmId = sFirmId;
	}
	public String getsCustomerId() {
		return sCustomerId;
	}
	public void setsCustomerId(String sCustomerId) {
		this.sCustomerId = sCustomerId;
	}
	public String getWhId() {
		return whId;
	}
	public void setWhId(String whId) {
		this.whId = whId;
	}
}
